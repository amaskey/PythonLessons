#Intermediate Level:

#Reshaping Arrays
#Refers to changing the structure (i.e., the number of rows, columns, and possibly more dimensions) of an array without changing its data.

import numpy as np
a = np.array([[1, 2, 3], [4, 5, 6]])
b = a.reshape(3, 2)
print(b)

#Matrix Multiplication

import numpy as np
a = np.array([[1, 2], [3, 4]])
b = np.array([[2, 0], [0, 2]])
result = np.dot(a, b)
print(result)

